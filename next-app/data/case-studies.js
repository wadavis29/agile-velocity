// Helper functions
export const getAllCaseStudies = () => caseStudies;
export const getFeaturedCaseStudies = () => caseStudies.filter(cs => cs.featured);
export const getCaseStudyById = (id) => caseStudies.find(cs => cs.id === id);
export const getCaseStudiesByIndustry = (industry) => caseStudies.filter(cs => cs.industry.toLowerCase().includes(industry.toLowerCase()));
export const getCaseStudiesByTag = (tag) => caseStudies.filter(cs => cs.tags.some(t => t.toLowerCase().includes(tag.toLowerCase())));

// Get unique industries
export const getIndustries = () => {
  const industries = [...new Set(caseStudies.map(cs => cs.industry))];
  return industries.sort();
};

// Get unique tags
export const getAllTags = () => {
  const tags = caseStudies.flatMap(cs => cs.tags);
  return [...new Set(tags)].sort();
};

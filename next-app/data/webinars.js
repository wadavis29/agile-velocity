// Helper function to get all unique categories
export const getCategories = () => {
  const categories = [...new Set(webinars.map(w => w.category))];
  return categories.sort();
};

// Helper function to get featured webinars
export const getFeaturedWebinars = () => {
  return webinars.filter(w => w.featured);
};

// Helper function to get webinar by slug
export const getWebinarBySlug = (slug) => {
  return webinars.find(w => w.slug === slug);
};

// Helper function to get related webinars (same category, excluding current)
export const getRelatedWebinars = (currentSlug, limit = 3) => {
  const current = getWebinarBySlug(currentSlug);
  if (!current) return [];

  return webinars
    .filter(w => w.slug !== currentSlug && w.category === current.category)
    .slice(0, limit);
};

// Helper function to search webinars
export const searchWebinars = (query) => {
  const searchTerm = query.toLowerCase();
  return webinars.filter(w =>
    w.title.toLowerCase().includes(searchTerm) ||
    w.description.toLowerCase().includes(searchTerm) ||
    w.topics.some(t => t.toLowerCase().includes(searchTerm)) ||
    w.presenters.some(p => p.toLowerCase().includes(searchTerm))
  );
};

export default webinars;
